import pytest

from cave import *


def test_Cave():
    cave = Cave(5, 10, 5)
    assert cave.map == [['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.']]
    assert cave.map_offset == 5

    add_to_cave(cave, 5, 0, '#')
    assert cave.map == [['#', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.']]

    remove_from_cave(cave, 5, 0)
    assert cave.map == [['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.'],
                        ['.', '.', '.', '.', '.', '.']]
